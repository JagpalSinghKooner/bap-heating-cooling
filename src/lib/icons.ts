/**
 * Get icon path by name, with alias support
 * @param name - Icon name or alias
 * @param fallback - Fallback icon name (default: 'check')
 */
export function getIconPath(name: string, fallback: IconName = 'check'): string {
  // Check direct match first
  if (name in iconPaths) {
    return iconPaths[name as IconName];
  }
  // Check aliases
  if (name in iconAliases) {
    return iconPaths[iconAliases[name]];
  }
  // Return fallback
  return iconPaths[fallback];
}
